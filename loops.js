function forLoop()
{
    var i;
    for (i = 1;i<5;1++)
    {
      alert("for loop - i = " + i);  
    }
}

function whileLoop()
{
   var i = 1;
   while (1 < 5)
    {
        alert("while loop - i = " + i);
        i++;
    } 
    alert("done with while loop");

}

function doLoop()
{
    var i = 5;
    do  
    {
      alert("do loop i = " + i); 
    }
    while (i <5);
}